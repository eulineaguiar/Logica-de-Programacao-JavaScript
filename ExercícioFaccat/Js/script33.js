// 33) Ler dois valores e imprimir uma mensagem
let valor1 = prompt("Digite o primeiro valor:");
let valor2 = prompt("Digite o segundo valor:");
if (valor1 == valor2) {
    alert("Números iguais");
} else if (valor1 > valor2) {
    alert("Primeiro é maior");
} else {
    alert("Segundo maior");
}